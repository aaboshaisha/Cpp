#include <iostream>
using namespace std;

int main()
{
    // Prompt the use to enter two numbers
    cout << "Enter two numbers a & b: \n";
    // Store user input in variables a & b
    int a, b;
    cin >> a >> b;
    cout << " a + b = " << a+b << endl;
    cout << " a - b = " << a-b << endl;
    cout << " a * b = " << a*b << endl;
    cout << " a / b = " << a/b << endl;

    return 0; 
}